package com.nickferraro.colorlovers.color;

public class RGB {
	private final int mRed;
	private final int mGreen;
	private final int mBlue;
	
	public RGB(int red, int green, int blue) {
		mRed = red;
		mGreen = green;
		mBlue = blue;
	}
	
	public int getRed() {
		return mRed;
	}
	public int getGreen() {
		return mGreen;
	}
	public int getBlue() {
		return mBlue;
	}
}
